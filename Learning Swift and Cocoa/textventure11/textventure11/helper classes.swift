import Foundation

public class Point {
    public var x: Int
    public var y: Int
    
    public init (x: Int, y: Int) {
        self.x = x
        self.y = y
    }
}

public class Size {
    public var width: Int
    public var height: Int
    
    public init (width: Int, height: Int) {
        self.width = width
        self.height = height
    }
}

